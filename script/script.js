/* ================================================
   LANGUAGE SYSTEM
================================================ */
let currentLang = localStorage.getItem('lang') || 'pt'
let typedInstance = null

function setLanguage(lang) {
  currentLang = lang
  localStorage.setItem('lang', lang)

  document.documentElement.lang = lang === 'pt' ? 'pt-br' : 'en'
  document.getElementById('langLabel').textContent = lang === 'pt' ? 'EN' : 'PT'

  const t = translations[lang]

  // Update all [data-i18n] elements (innerHTML for tags like <strong>)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n')
    if (t[key] !== undefined) el.innerHTML = t[key]
  })

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder')
    if (t[key] !== undefined) el.placeholder = t[key]
  })

  // Switch typed strings
  const strPt = document.getElementById('typed-strings-pt')
  const strEn = document.getElementById('typed-strings-en')
  strPt.style.display = lang === 'pt' ? '' : 'none'
  strEn.style.display = lang === 'en' ? '' : 'none'

  // Re-init Typed.js
  if (typedInstance) typedInstance.destroy()
  document.getElementById('typed').textContent = ''

  typedInstance = new Typed('#typed', {
    stringsElement: lang === 'pt' ? '#typed-strings-pt' : '#typed-strings-en',
    typeSpeed: 55,
    backSpeed: 30,
    backDelay: 2200,
    loop: true
  })
}

// Language toggle
document.getElementById('langSwitcher').addEventListener('click', () => {
  setLanguage(currentLang === 'pt' ? 'en' : 'pt')
})

// Init on load
setLanguage(currentLang)

/* ================================================
   NAVBAR SCROLL
================================================ */
const navbar = document.getElementById('navbar')
window.addEventListener('scroll', () => {
  navbar.classList.toggle('scrolled', window.scrollY > 50)
})

/* ================================================
   MOBILE NAV
================================================ */
const navToggle = document.getElementById('navToggle')
const navLinks  = document.getElementById('navLinks')

navToggle.addEventListener('click', () => {
  navLinks.classList.toggle('open')
  navToggle.setAttribute('aria-expanded', navLinks.classList.contains('open'))
})

document.querySelectorAll('.nav-link').forEach(link => {
  link.addEventListener('click', () => navLinks.classList.remove('open'))
})

/* ================================================
   ACTIVE NAV ON SCROLL
================================================ */
const sections     = document.querySelectorAll('section[id]')
const navLinkItems = document.querySelectorAll('.nav-link')

new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return
    navLinkItems.forEach(link => {
      link.classList.toggle('active', link.getAttribute('href') === `#${entry.target.id}`)
    })
  })
}, { threshold: 0.3, rootMargin: '-68px 0px 0px 0px' }).observe
  && sections.forEach(s =>
    new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (!entry.isIntersecting) return
        navLinkItems.forEach(link => {
          link.classList.toggle('active', link.getAttribute('href') === `#${entry.target.id}`)
        })
      })
    }, { threshold: 0.3, rootMargin: '-68px 0px 0px 0px' }).observe(s)
  )

